    @classmethod
    def get_all_names(cls):
        """获取所有图标名称列表"""
        return [member.value for member in cls]

    @classmethod
    def search(cls, keyword: str):
        """
        搜索图标

        Args:
            keyword: 搜索关键词

        Returns:
            匹配的图标枚举列表
        """
        keyword = keyword.lower()
        return [member for member in cls if keyword in member.value.lower()]